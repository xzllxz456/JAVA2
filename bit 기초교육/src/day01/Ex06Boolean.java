package day01;
// boolean 데이터타입은
// 참/거짓을 뜻하는 true/false 2가지의 값만 가질 수 있다.
// boolean은 우리가 변수를 그대로 쓰는 경우보다는
// 나중에 배울 연산자의 결과값을 그대로 사용하는 경우가 더 많다.
// 특히 조건문과 반복문에서 boolean 결과값이 쓰이게 된다.
public class Ex06Boolean {
	public static void main(String[] args) {
		boolean myBoolean = true;
		System.out.println("myBoolean의 현재값: "+myBoolean);
		myBoolean = false;
		System.out.println("myBoolean의 현재값: "+myBoolean);
	}
}
